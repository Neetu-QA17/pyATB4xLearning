squares = [1, 4, 9, 16, 25]
print(squares)

# POP - will remove/pop-out the last element in the list
# working like a stack
squares.pop()
print(squares)

# List is mutable in nature
# mutable means - that it can be changed
squares[2] = 64   # here value at index[2] i.e. 9 is replaced with 64
print(squares)
